package com.dataserve.se.bean;

public class ChoiceListBean {

	private String lang;
	private String dispName;
	private String value;
	private String depOn;
	private String depValue;
	
	
	public String getLang() {
		return lang;
	}
	public void setLang(String lang) {
		this.lang = lang;
	}
	public String getDispName() {
		return dispName;
	}
	public void setDispName(String dispName) {
		this.dispName = dispName;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	public String getDepOn() {
		return depOn;
	}
	public void setDepOn(String depOn) {
		this.depOn = depOn;
	}
	public String getDepValue() {
		return depValue;
	}
	public void setDepValue(String depValue) {
		this.depValue = depValue;
	}
	
	

}
